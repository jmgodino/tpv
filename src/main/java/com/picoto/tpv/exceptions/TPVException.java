package com.picoto.tpv.exceptions;

public class TPVException extends RuntimeException {

	private static final long serialVersionUID = -8123465227066968476L;

	public TPVException() {
		super();
	}

	public TPVException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TPVException(String arg0) {
		super(arg0);
	}

	public TPVException(Throwable arg0) {
		super(arg0);
	}

	
	
}
